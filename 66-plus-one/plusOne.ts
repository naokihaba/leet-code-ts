/**
 * 66. Plus One（プラスワン）
 *
 * 大きな整数が整数配列 digits として表現されています。
 * digits[i] はその整数の i 番目の桁を表します。
 * 桁は左から右へ、最上位桁から最下位桁の順に並んでいます。
 * この大きな整数には先頭に 0 が含まれません。
 *
 * この大きな整数に 1 を加算し、結果の桁の配列を返してください。
 *
 * 例1:
 * 入力: digits = [1,2,3]
 * 出力: [1,2,4]
 * 説明: 配列は整数 123 を表しています。
 *       1 を加算すると 123 + 1 = 124 になります。
 *       したがって、結果は [1,2,4] です。
 *
 * 例2:
 * 入力: digits = [4,3,2,1]
 * 出力: [4,3,2,2]
 * 説明: 配列は整数 4321 を表しています。
 *       1 を加算すると 4321 + 1 = 4322 になります。
 *       したがって、結果は [4,3,2,2] です。
 *
 * 例3:
 * 入力: digits = [9]
 * 出力: [1,0]
 * 説明: 配列は整数 9 を表しています。
 *       1 を加算すると 9 + 1 = 10 になります。
 *       したがって、結果は [1,0] です。
 *
 * 制約:
 * - 1 <= digits.length <= 100
 * - 0 <= digits[i] <= 9
 * - digits には先頭の 0 が含まれません。
 *
 * ヒント:
 * - 配列の末尾（最下位桁）から処理を開始する
 * - 繰り上がり（キャリー）の処理が重要
 * - すべての桁が 9 の場合（例: [9,9,9] → [1,0,0,0]）に注意
 *
 */
export function plusOne(digits: number[]): number[] {
  // 末尾から逆にループする
  for (let i = digits.length - 1; i >= 0; i--) {

    // 9未満の場合は繰り上げなし。終了
    if (digits[i] < 9) {
      digits[i]++
      return digits
    }

    // 繰り上げの場合は0が最後に来る
    digits[i] = 0
  }

  // [9]・[9,9] = [0,0]になるので先頭に1を追加
  digits.unshift(1)

  return digits
}
